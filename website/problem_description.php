<!--<MarkdownReplacement with="competition-Problem-Description.md">--><h1>Ants Problem Description</h1>

<p>The objective is to create a computer program that plays the game of Ants as intelligently as possible. It is recommended that you use one of the starter packages as a starting point. If you are looking to get up and running as quickly as possible, check out the Five Minute Quickstart Guide. For more details about Ants beyond this introductory document, see the Ants Specification.</p>

<p>Ants is a strategy game set on a rectangular grid with the edges wrapped. The objective is to take over all the resources by eliminating all of the enemy colonies.</p>

<p>The game is turn-based. Your bot is a program that inputs information about squares it can currently see and outputs orders to move any number of their ants either north, east, south or west. </p>

<p>Your bot can issue up to one order for each of its ants during one turn. Each order specifies an ant location and direction to move it. Once the order is executed, ants move one square in the given direction. Your bot issues orders by outputting this information in the format outlined on the specifications page.</p>

<p>The game ends when only one player remains, or if the game goes past a certain number of turns.</p>
