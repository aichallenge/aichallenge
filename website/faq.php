<h2>Frequently Asked Questions</h2>
<p><strong>Question:</strong> I am stuck and/or stuff isn't working. Where
  can I get some help or information?</p>
<p><strong>Answer:</strong> We welcome your post on the
  <a href="http://ai-contest.com/forums">forums</a>!</p>
<p><strong>Question:</strong> What is the relationship between Google and the
  University of Waterloo Computer Science Club?</p>
<p><strong>Answer:</strong> The University of Waterloo Computer Science Club
  is a student-run club at the University of Waterloo with about 300 members,
  most of whom are undergraduate students. The CSC puts on many events at UW,
  including talks by distinguished speakers, tutorials, workshops, programming
  contests, and social events.</p>
<p>The contest website, backend, and advertising is all done by student
  volunteers, mostly from the University of Waterloo Computer Science Club.
  Google helps us out by sponsoring the contest, and helps us to spread the
  word at other universities around the world so that more people can take
  part.</p>
<p><strong>Question:</strong> Where can I see some strategy ideas?</p>
<p><strong>Answer:</strong> Check out the <a href="/resources.php">articles and
  tutorials section</a>, where you will find a couple strategy guides, as well
  as the <a href="/forums/">forums</a>.</p>
<p><strong>Question:</strong> Do we have to submit PlanetWars.java/
  PlanetWars.cc? Can we have more source files than the ones that come in
  the starter package? How does the auto-compile work?</p>
<p><strong>Answer:</strong> You have to submit any and all source files that
  your entry requires in order to run, including
  PlanetWars.java/PlanetWars.cc. You can
  have as many source files as you want, as long as they're all in the same
  language and they're all in the same directory. The auto-compile script looks
  at which files you submitted to auto-detect the language that you're using,
  identifies all the source files you submitted, compiles each one, then links
  them together. Check out the email you get after you submit a starter package
  to see the exact commands that the auto-compile script uses to compile your
  code. Post to the forums if you need any help, and we'll be glad to address
  your specific case.</p>
<p><strong>Question:</strong>May I modify the provided code?</p>
<p><strong>Answer:</strong>You may modify any of the provided files in a starter
  package, including the PlanetWars class. You may even delete them if your code does
  not require them, with the sole exception of MyBot.*. This file may me
  modified but not deleted.</p>
<p><strong>Question:</strong> How do the rankings work?</p>
<p><strong>Answer:</strong> The rankings are updated once every 60 seconds.
  The rankings are calculated using the bayeselo program. The rating system
  is similar to that which is used in professional chess.</p>
<p><strong>Question:</strong> Can I have more than one account?</p>
<p><strong>Answer:</strong> No. People found to be in control of two or more
  accounts will be forever banished.</p>
<p><strong>Question:</strong> Can we make teams?</p>
<p><strong>Answer:</strong> Yes, but say so in the "bio" field when you create
  the team's account.</p>
<p><strong>Question:</strong> There is no starter package for my favorite
  language. What shall I do?</p>
<p><strong>Answer:</strong> You don't know C++, Java, or Python? Okay
  fine. Tell the forums what starter package you want to see, and we will
  try our best to make it for you.</p>
<p><strong>Question:</strong> Can contest staff participate in the contest?</p>
<p><strong>Answer:</strong> No. There are about two dozen people who have
  contributed their technical expertise to the contest, and none of these
  people will be participating. Furthermore, any person with access to the
  live production environment cannot compete, such as the CSC Sytems Committee
  or CSC Executive. Occasionally, you may see a test account in the rankings
  which is controlled by a member of contest staff. Have no fear, these bots
  cannot win the contest.</p>
<p><strong>Question:</strong> Are there any prizes?</p>
<p><strong>Answer:</strong> Maybe. We're working on that. There is of course
  the glory and bragging rights that come with doing well in the best
  programming contest ever made.</p>
