<h1 id="ants-five-minute-quickstart-guide">Ants Five Minute Quickstart Guide</h1>
<p>This five-minute quickstart guide will guide you through the steps to get your name on the global leaderboard in less than five minutes. There is also a Next Steps section to give you some idea which direction to go after you've completed this guide.</p>
<h2 id="1-create-an-account">1. Create an Account</h2>
<p>Create an account by filling out the <a href="http://aichallenge.org/register.php">signup form</a> .</p>
<h2 id="2-activate-your-account">2. Activate Your Account</h2>
<p>Once your account is created, you must activate it by clicking the link in the confirmation email. If you haven't received a confirmation email in your inbox after five minutes, check your junk mail folder. </p>
<h2 id="3-sign-in-to-the-website">3. Sign in to the Website</h2>
<p>You can sign in by clicking the <a href="http://aichallenge.org/login.php">Sign In</a> link at the top of the website.</p>
<h2 id="4-download-a-starter-package">4. Download a Starter Package</h2>
<p>The <a href="starter_packages.php">starter packages are here</a>.</p>
<p>At this stage, it doesn't matter which starter package you choose, since we won't be modifying it yet. Choose any starter package, and download it. The starter packages are all ZIP files, and you should now have one of these ZIP files on your computer.</p>
<h2 id="5-submit-the-starter-package">5. Submit the Starter Package</h2>
<p>Don't bother unzipping or modifying the starter package you just downloaded. Make sure that you're <a href="http://aichallenge.org/login.php">signed in</a> to the website, visit the <a href="http://aichallenge.org/submit.php">Upload Your Code</a> page, and submit the starter package (ZIP file) that you just downloaded.</p>
<h2 id="6-youre-done">6. You're Done!</h2>
<p>Since you've submitted an entry, your name will soon appear on the global leaderboard. It can take up to an hour for your name to appear, but usually only takes a few minutes.</p>
<h2 id="next-steps">Next Steps</h2>
<p>Now that you've submitted your first basic working entry, it's time to get your hands dirty, and start customizing your own strategy. Here are some places to look for guidance as you move forward on your quest to the top of the leaderboard!</p>
<ul>
